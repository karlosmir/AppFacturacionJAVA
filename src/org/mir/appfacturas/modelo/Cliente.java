package org.mir.appfacturas.modelo;

public class Cliente {
    // Atributos
    private String nombre;
    private String DNI;

    // GET - SET
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDNI() {
        return DNI;
    }

    public void setDNI(String DNI) {
        this.DNI = DNI;
    }
}
